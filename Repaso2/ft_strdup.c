#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;

	while (src[i])
		i++;
	new = (char *)malloc(sizeof(char) * (i + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	return (new);
}
