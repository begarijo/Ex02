#include <unistd.h>

int	is_space(char c)
{
	if (c == ' ' || c == '\t')
		return (1);
	return (0);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			if (is_space(argv[1][i]))
				write(1, "  ", 2);
			write(1, &argv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
